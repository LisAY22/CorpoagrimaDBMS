    private void actualizarTabla() {
        try {
            ResultSet rs = clientes.consulta(conexion);

            // Obtener el modelo de la tabla actual
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Limpiar los datos existentes

            // Agregar nuevas filas al modelo de tabla
            ResultSetMetaData metaData = rs.getMetaData();
            int columnCount = metaData.getColumnCount();
            while (rs.next()) {
                Object[] rowData = new Object[columnCount];
                for (int i = 0; i < columnCount; i++) {
                    // Obtener el valor de la columna
                    Object value = rs.getObject(i + 1);
                    if (i == 6) { 
                        if (value instanceof Integer) {
                            int intValue = (int) value;
                            switch (intValue) {
                                case 0:
                                    rowData[i] = "-";
                                    break;
                                case 1:
                                    rowData[i] = "\u2605"; // C√≥digo Unicode para una estrella
                                    break;
                                default:
                                    rowData[i] = value;
                                    break;
                            }
                        } else {
                            rowData[i] = value == null ? "-" : value; // Si el valor es null, colocamos un guion "-"
                        }
                    } else {
                        rowData[i] = value == null ? "-" : value; // Si el valor es null, colocamos un guion "-"
                    }
                }
                model.addRow(rowData);
            }

            rs.close();

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }




